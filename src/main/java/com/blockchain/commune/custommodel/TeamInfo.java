package com.blockchain.commune.custommodel;

public class TeamInfo {
    private String userId;
    private String superUserId;
    private String memberAmount;
    private String directMemberAmount;
    private String memberLevel;
    private String presidentAmount;
    private String directPresidentAmount;
    private String superPhone;
    private String superEmail;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getSuperUserId() {
        return superUserId;
    }

    public void setSuperUserId(String superUserId) {
        this.superUserId = superUserId;
    }

    public String getMemberAmount() {
        return memberAmount;
    }

    public void setMemberAmount(String memberAmount) {
        this.memberAmount = memberAmount;
    }

    public String getDirectMemberAmount() {
        return directMemberAmount;
    }

    public void setDirectMemberAmount(String directMemberAmount) {
        this.directMemberAmount = directMemberAmount;
    }

    public String getMemberLevel() {
        return memberLevel;
    }

    public void setMemberLevel(String memberLevel) {
        this.memberLevel = memberLevel;
    }

    public String getPresidentAmount() {
        return presidentAmount;
    }

    public void setPresidentAmount(String presidentAmount) {
        this.presidentAmount = presidentAmount;
    }

    public String getDirectPresidentAmount() {
        return directPresidentAmount;
    }

    public void setDirectPresidentAmount(String directPresidentAmount) {
        this.directPresidentAmount = directPresidentAmount;
    }

    public String getSuperPhone() {
        return superPhone;
    }

    public void setSuperPhone(String superPhone) {
        this.superPhone = superPhone;
    }

    public String getSuperEmail() {
        return superEmail;
    }

    public void setSuperEmail(String superEmail) {
        this.superEmail = superEmail;
    }
}
